<h1 class="text-center text-4xl font-extrabold my-2">The why behind Cognnect</h1>
<p class="text-center text-lg px:4 md:px-20">
	School is so stressful, especially at Math Sci. On top of all the homework we have to keep in the
	back of our minds, we also have extracurriculars, studying, and (hopefully) a life outside of
	school. We are trying to make that as easy to do as possible, by trying to eliminate some of the
	boilerplate of school.
</p>
<h2 class="text-center text-2xl font-bold my-2 mt-6">Why us over existing apps?</h2>
<p class="text-center text-lg px:4 md:px-20">
	There are dozens of existing apps out there looking to help you out at school, in fact there's a
	whole industry: edtech. But rather than using Quizlet with Canvas and Studentvue, where each one
	works seperately, it would be so much more intuitive if you could just use them all at once. Have
	an English test coming up? Don't worry, just click on it in your to-do list and there will be
	flashcards already made for you! We also have the advantage of being students, rather than
	business owners. We originally made this app to help US with school, not to sell to a target
	audience. We have the advantage of seeing what we are missing, because we are the users of our
	app. So of course we listen to our customers, because we ARE our customers.
</p>

